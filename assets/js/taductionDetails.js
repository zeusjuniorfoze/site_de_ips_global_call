function updateServiceContent(index, lang) {
  const service = services[index];
  document.getElementById("service-title").textContent =
    translations[lang][service.title];
  document.getElementById("service-description-1").textContent =
    translations[lang][service.description1];
  const featuresList = document.getElementById("service-features");
  featuresList.innerHTML = service.features
    .map(
      (feature) =>
        `<li><i class="bi bi-check-circle"></i> <span>${translations[lang][feature]}</span></li>`
    )
    .join("");
  document.getElementById("service-description-2").textContent =
    translations[lang][service.description2];
}

