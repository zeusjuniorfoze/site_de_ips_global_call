// Language Switcher
function setLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang][key]) {
      element.innerText = translations[lang][key];
    } else {
      console.warn(`Translation key "${key}" not found for language "${lang}"`);
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    } else {
      console.warn(
        `Placeholder translation key "${key}" not found for language "${lang}"`
      );
    }
  });
  document.documentElement.lang = lang;
  localStorage.setItem("language", lang);
  document.querySelectorAll(".change-lang").forEach((item) => {
    item.classList.remove("active");
    if (item.getAttribute("data-lang") === lang) {
      item.classList.add("active");
    }
  });
  document
    .querySelector('meta[name="description"]')
    .setAttribute("content", translations[lang].meta_description);
  document.title = translations[lang].meta_title;
  const activeIndex =
    document
      .querySelector(".swiper-slide-active")
      ?.getAttribute("data-service-id") || 0;
  updateServiceContent(activeIndex, lang);
}

