// Language Switcher
function setLanguage(lang) {
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (translations[lang][key]) {
      element.innerText = translations[lang][key];
    }
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });
  document.documentElement.lang = lang;
  localStorage.setItem("language", lang);
  document.querySelectorAll(".change-lang").forEach((item) => {
    item.classList.remove("active");
    if (item.getAttribute("data-lang") === lang) {
      item.classList.add("active");
    }
  });
  document
    .querySelector('meta[name="description"]')
    .setAttribute("content", translations[lang].meta_description);
  document.title = translations[lang].meta_title;
}

document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("language") || "fr";
  setLanguage(savedLang);
  document.querySelectorAll(".change-lang").forEach((button) => {
    button.addEventListener("click", (e) => {
      e.preventDefault();
      const lang = button.getAttribute("data-lang");
      setLanguage(lang);
    });
  });
});

// Smooth Scroll
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    const href = this.getAttribute("href");
    if (href && href !== "#" && !this.classList.contains("change-lang")) {
      // Skip language links and invalid href
      e.preventDefault();
      const target = document.querySelector(href);
      if (target) {
        // Ensure target exists
        target.scrollIntoView({
          behavior: "smooth",
        });
      }
    }
  });
});

// Optimized Parallax Effect
let lastScrollY = 0;
function updateParallax() {
  const parallaxSections = document.querySelectorAll(".parallax");
  parallaxSections.forEach((section) => {
    section.style.backgroundPositionY = lastScrollY * 0.3 + "px";
  });
  requestAnimationFrame(updateParallax);
}
window.addEventListener("scroll", () => {
  lastScrollY = window.pageYOffset;
});
requestAnimationFrame(updateParallax);

